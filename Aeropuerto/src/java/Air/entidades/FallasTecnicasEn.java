
package Air.entidades;


public class FallasTecnicasEn {
    
    private String matriculaAvion;
    private String descripcionDano;
    private String cedulaMecanico;
    private String nombreMecanico;
    private String especialidadMecanico;
    private String categoriaEspecialidad;

    public String getMatriculaAvion() {
        return matriculaAvion;
    }

    public void setMatriculaAvion(String matriculaAvion) {
        this.matriculaAvion = matriculaAvion;
    }

    public String getDescripcionDano() {
        return descripcionDano;
    }

    public void setDescripcionDano(String descripcionDano) {
        this.descripcionDano = descripcionDano;
    }

    public String getCedulaMecanico() {
        return cedulaMecanico;
    }

    public void setCedulaMecanico(String cedulaMecanico) {
        this.cedulaMecanico = cedulaMecanico;
    }

    public String getNombreMecanico() {
        return nombreMecanico;
    }

    public void setNombreMecanico(String nombreMecanico) {
        this.nombreMecanico = nombreMecanico;
    }

    public String getEspecialidadMecanico() {
        return especialidadMecanico;
    }

    public void setEspecialidadMecanico(String especialidadMecanico) {
        this.especialidadMecanico = especialidadMecanico;
    }

    public String getCategoriaEspecialidad() {
        return categoriaEspecialidad;
    }

    public void setCategoriaEspecialidad(String categoriaEspecialidad) {
        this.categoriaEspecialidad = categoriaEspecialidad;
    }
    
}
